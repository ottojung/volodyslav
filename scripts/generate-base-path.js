#!/usr/bin/env node

/**
 * Generates backend/src/base_path.js with the base path prefix extracted
 * from the VOLODYSLAV_BASEURL environment variable (set at build time).
 * If VOLODYSLAV_BASEURL is not set, the prefix defaults to "" (root).
 */

"use strict";

const fs = require("fs");
const path = require("path");

const baseUrlEnv = process.env["VOLODYSLAV_BASEURL"] || "";

let basePath = "";
if (baseUrlEnv) {
    try {
        const url = new URL(baseUrlEnv);
        basePath = url.pathname.replace(/\/$/, "");
    } catch {
        process.stderr.write(
            `Warning: VOLODYSLAV_BASEURL="${baseUrlEnv}" is not a valid URL. Using root path.\n`
        );
    }
}

const content = `// This file is generated by scripts/generate-base-path.js during \`npm run build\`.
// Do not edit manually; re-run \`npm run build\` to update it.
// The version committed to git contains the default (empty string = root) used
// during development and testing. Deployments with VOLODYSLAV_BASEURL set will
// have this file overwritten by the build step with the correct path prefix.

/**
 * The base path prefix extracted from VOLODYSLAV_BASEURL at build time.
 * Empty string means the app is served from root.
 * @type {string}
 */
const BASE_PATH_PREFIX = ${JSON.stringify(basePath)};

module.exports = { BASE_PATH_PREFIX };
`;

const outputPath = path.join(__dirname, "..", "backend", "src", "base_path.js");
fs.writeFileSync(outputPath, content);
process.stdout.write(`Generated base_path.js with prefix: ${JSON.stringify(basePath)}\n`);
